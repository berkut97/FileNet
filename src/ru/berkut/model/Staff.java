/**
 * Создание класса абстрактного класса Элемент организационной структуры
 */
public abstract class Staff {
	private String StaffID;
	
	public String getStaffID() {
		return StaffID;
	}
	public void setStaffID(String StaffID) {
		this.StaffID = StaffID;
	}
}
